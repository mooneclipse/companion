"""
環境変数とアプリケーション設定の読み込み
"""
import logging
from pathlib import Path
from typing import Literal

from pydantic import Field, ValidationError
from pydantic_settings import BaseSettings, SettingsConfigDict

logger = logging.getLogger(__name__)


class Settings(BaseSettings):
    """
    アプリケーション設定管理クラス
    .envファイルから環境変数を読み込み、型チェックと検証を行う
    """
    # --- ログ設定 ---
    LOG_LEVEL: Literal["DEBUG", "INFO", "WARNING", "ERROR", "CRITICAL"] = "INFO"

    # --- 必須APIキー ---
    YOUTUBE_API_KEY: str = Field(..., min_length=1, description="Google Cloud Consoleで取得したYouTube Data API v3キー")

    # --- アプリケーション設定 ---
    # 毎日実行前提のため 2 日（取りこぼしマージン込み）。
    # JSON 側の個別 check_days が優先される
    CHECK_DAYS: int = Field(
        2, ge=1, le=30,
        description="動画を取得する期間（日数、毎日実行の取りこぼしマージン込み）",
    )
    MAX_CONCURRENT_TASKS: int = Field(5, ge=1, le=20, description="並列処理の最大数")

    # プロンプト設定
    EVALUATION_PROMPT_TEMPLATE: str = """あなたはVTuber・配信者の配信を長年見てきたベテランリスナーです。
以下はYouTube配信の自動生成字幕です。この字幕を読んで、配信が面白かったかどうかを評価してください。

【動画情報】
タイトル: {title}

【字幕テキスト分析メモ】
{excitement_summary}

【字幕テキスト】
{subtitle_text}

【評価の観点】（重要度順）
1. **テンション・空気感**: 配信者が楽しそうか？笑ったり驚いたりする瞬間があるか？
2. **盛り上がりポイント**: 字幕から読み取れる盛り上がりシーンはいくつあるか？
3. **トーク・掛け合い**: 面白い発言、ツッコミ、独特のリアクションがあるか？
4. **配信ジャンル補正**:
   - 雑談配信: エピソードトークの面白さ、話の展開の巧さ
   - ゲーム実況: リアクション、プレイの見どころ、実況の面白さ
   - 歌枠: 選曲の良さ、歌唱力、MCの面白さ
   - コラボ: メンバー同士の化学反応、掛け合いの面白さ
   - 企画: 耐久配信・視聴者参加企画など特定の目的がある配信
   - その他: 作業配信・ASMR・同時視聴など上記に当てはまらないもの
5. **また見たいか**: この配信者のこの回を「見逃したら絶対に損」と思うか？
6. **独自性・クリエイティビティ**: この配信者ならではの企画や発想、ユニークな視点があるか？
7. **安定感・構成力**: トークの聞き心地、配信全体のフリ・オチの構成力があるか？
8. **キャラクター性（VTuber）**: そのVTuberならではのRP・個性が発揮されているか？

【重要な注意事項】
- 人気かどうかは関係ありません。純粋に内容の面白さだけで判断してください。
- 自動生成字幕は不正確なことがあります。文脈から推測して評価してください。
- 字幕は冒頭・中盤・終盤の3箇所からの抜粋の場合があります（「...(中略)...」区切り）。
- 以下に該当する配信は低スコアにしてください:
  - 「告知だけ」「テスト配信」「枠取り」など実質的に内容がない配信
  - 単調で起伏がない（笑いも驚きも感動もない）配信
  - 不快な発言や雰囲気が続く配信
  - 配信トラブルや技術的問題が長く続いた配信
- 上記の「字幕テキスト分析メモ」は参考指標です。スコアはあなた自身の判断を優先してください。

【表現ルール】
評価コメントは「観察者目線のフラットな記述」で書く。以下の表現は使用禁止:

- 上から目線・断定的否定: 「物足りない」「爆発力には欠ける」「水準止まり」「水準以上」「絶対損には届かず」「断言できない」「〜とまでは言いにくい」「〜とは言い切れない」
- 自己言及・言い訳: 「字幕の範囲では」「字幕断片からは」「字幕省略のため」「字幕の精度が低く」「字幕情報が極端に少なく」「字幕からは判断不可」

書き換え方針:
- ❌「〜が物足りない」 → ⭕「〜という構成」「〜という展開」
- ❌「絶対見逃せない級ではない」 → ⭕「見どころは X、Y」
- ❌「字幕の範囲では○○が読み取れない」 → ⭕（その動画は評価対象外として「字幕で判断不可」を返す）

【字幕で判断不可の場合】
字幕が極端に少ない、内容が掴めない、自己言及せずには評価コメントを書けない場合は、
スコアを 0 とし、理由欄に **「字幕で判断不可」** とのみ書いてください（言い訳は書かない）。
このフラグは後段でレポートから除外されます。

【良い評価例】
例1（ゲーム実況・スコア8）:
{{"score": 8, "genre": "ゲーム実況", "highlights": ["ボス戦中盤の○○リアクション", "終盤の予想外ルート", "エンディング後の感想トーク"], "reason": "ボス突入から終盤まで笑いと驚きが連続している構成。リアクションのテンポが良くプレイ実況として完成度が高い。"}}

例2（雑談・スコア6）:
{{"score": 6, "genre": "雑談", "highlights": ["序盤のエピソード「○○」", "中盤のリスナー回答コーナー"], "reason": "エピソードトーク主体の落ち着いた構成。終盤は告知中心になる流れ。"}}

例3（評価対象外・字幕不足）:
{{"score": 0, "genre": "その他", "highlights": [], "reason": "字幕で判断不可"}}

【出力形式】
次のキーを持つ JSON オブジェクト**のみ**を出力してください（前後の説明文・コードフェンスは付けない）:
- "score": 整数 0-10（10が最高。8以上は「見逃したら絶対に損」レベル。7は「時間があれば見たい」レベル）
- "genre": "雑談" / "ゲーム実況" / "歌枠" / "コラボ" / "企画" / "その他" から1つ
- "highlights": 字幕から読み取れた盛り上がりポイントの文字列配列（最大3つ）。具体的な発言や状況がわかるよう記述すること（例: 「〇〇という場面でリアクションが爆発」「序盤の〇〇トークが特に面白かった」）
- "reason": なぜこのスコアなのか、1-2文で
"""

    # SMALL 層チャンネル用評価プロンプト（〜1万登録、原石探し用）
    # 設計書「ytcheck-tier-eval-design.md」付録のプロンプト草案ベース。
    # MID/LARGE は既存 EVALUATION_PROMPT_TEMPLATE を継続使用するため、ここを変更しても影響しない。
    SMALL_CHANNEL_PROMPT_TEMPLATE: str = """あなたはVTuber・配信者の配信を長年見てきたベテランリスナーです。
これは登録者数1万人未満の小規模チャンネルの配信です。
大手とは違う「原石」を見つけるつもりで、以下の字幕を読んでください。

【動画情報】
タイトル: {title}

【字幕テキスト分析メモ】
{excitement_summary}

【字幕テキスト】
{subtitle_text}

【評価の観点】（重要度順、小規模チャンネル向け）
1. **熱量・本気度**: 配信者が本気で楽しんでいるか、全力で取り組んでいるか
2. **独自性・等身大の魅力**: その人ならではの発想・ニッチな趣味・リアルな素の魅力があるか
3. **実験性・挑戦**: 大手がやらないジャンル、初めての試み、手探りの面白さがあるか
4. **配信ジャンル補正**: 雑談/ゲーム実況/歌枠/コラボ/企画/その他
5. **また見たいか**: 「また会いに来たい」と思える人柄・配信スタイルか

【重要な注意事項】
- トーク技術の安定感・構成力は評価対象から外してください。荒削りでも本気ならOKです。
- 1つでも「本気のシーン」「独自の魅力」が見つかれば加点してください。
- 以下は引き続き低スコア:
  - 「告知だけ」「テスト配信」「枠取り」など実質的に内容がない配信
  - 不快な発言や雰囲気が続く配信
- 自動生成字幕は不正確なことがあります。文脈から推測してください。
- 字幕は冒頭・中盤・終盤の3箇所からの抜粋の場合があります（「...(中略)...」区切り）。

【表現ルール】
評価コメントは「観察者目線のフラットな記述」で書く。以下の表現は使用禁止:

- 上から目線・断定的否定: 「物足りない」「爆発力には欠ける」「水準止まり」「水準以上」「絶対損には届かず」「断言できない」「〜とまでは言いにくい」「〜とは言い切れない」
- 自己言及・言い訳: 「字幕の範囲では」「字幕断片からは」「字幕省略のため」「字幕の精度が低く」「字幕情報が極端に少なく」「字幕からは判断不可」

書き換え方針:
- ❌「〜が物足りない」 → ⭕「〜という構成」「〜という展開」
- ❌「絶対見逃せない級ではない」 → ⭕「見どころは X、Y」
- ❌「字幕の範囲では○○が読み取れない」 → ⭕（その動画は評価対象外として「字幕で判断不可」を返す）

【字幕で判断不可の場合】
字幕が極端に少ない、内容が掴めない、自己言及せずには評価コメントを書けない場合は、
スコアを 0 とし、理由欄に **「字幕で判断不可」** とのみ書いてください（言い訳は書かない）。
このフラグは後段でレポートから除外されます。

【良い評価例】
例1（ゲーム実況・スコア8）:
{{"score": 8, "genre": "ゲーム実況", "highlights": ["ボス戦中盤の○○リアクション", "終盤の予想外ルート", "エンディング後の感想トーク"], "reason": "ボス突入から終盤まで笑いと驚きが連続している構成。リアクションのテンポが良くプレイ実況として完成度が高い。"}}

例2（雑談・スコア6）:
{{"score": 6, "genre": "雑談", "highlights": ["序盤のエピソード「○○」", "中盤のリスナー回答コーナー"], "reason": "エピソードトーク主体の落ち着いた構成。終盤は告知中心になる流れ。"}}

例3（評価対象外・字幕不足）:
{{"score": 0, "genre": "その他", "highlights": [], "reason": "字幕で判断不可"}}

【出力形式】
次のキーを持つ JSON オブジェクト**のみ**を出力してください（前後の説明文・コードフェンスは付けない）:
- "score": 整数 0-10（10が最高。6以上で「拾い物候補」レベル）
- "genre": "雑談" / "ゲーム実況" / "歌枠" / "コラボ" / "企画" / "その他" から1つ
- "highlights": 字幕から読み取れた「本気のシーン」「独自の魅力」の文字列配列（最大3つ）
- "reason": なぜこのスコアなのか、1-2文で
"""

    # トークン制限
    MAX_INPUT_TOKENS: int = 1500  # claude -p の処理時間短縮のため削減（元: 5000）
    MAX_OUTPUT_TOKENS: int = 500

    # --- AI評価モデル設定 ---
    # 推し度が低い（favorite<=2）チャンネルの評価に使う軽量モデル。
    # 推薦閾値 7-8 で期待値が低い層のためコスト優先で Haiku に振り分ける。
    # favorite 3 以上は --model を指定せず現行デフォルトモデルを使う。
    LOW_FAVORITE_MODEL: str = Field(
        "claude-haiku-4-5",
        description="favorite 1-2 チャンネルの評価に使う claude -p の --model 値",
    )

    # --- バズ検知（🚀 伸びてる バッジ）設定 ---
    # published_at から BUZZ_WINDOW_HOURS 時間以内 かつ
    # view_count / subscriber_count >= BUZZ_VIEW_RATIO の動画にバッジを付ける。
    # スコアへの加点はしない（バッジ表示のみ）
    BUZZ_VIEW_RATIO: float = Field(
        0.5, gt=0,
        description="バズ判定の 再生数/登録者数 比の閾値",
    )
    BUZZ_WINDOW_HOURS: int = Field(
        48, ge=1,
        description="バズ判定の対象とする投稿からの経過時間（時間）",
    )

    # --- 推薦閾値設定 ---
    BASE_RECOMMEND_THRESHOLD: int = Field(
        7, ge=1, le=10,
        description="基本推薦閾値（好き度=2の場合のスコア）"
    )
    FAVORITE_THRESHOLD_MAP: dict[int, int] = Field(
        default_factory=lambda: {1: 8, 2: 7, 3: 6, 4: 5, 5: 3},
        description="好き度 -> 推薦閾値のマッピング"
    )

    # SMALL 層チャンネル専用の推薦閾値マップ（既存 FAVORITE_THRESHOLD_MAP より各値 -1 で甘め）
    SMALL_CHANNEL_THRESHOLD_MAP: dict[int, int] = Field(
        default_factory=lambda: {1: 7, 2: 6, 3: 5, 4: 4, 5: 2},
        description="SMALL層用 好き度 -> 推薦閾値のマッピング（標準より甘め、拾い物候補抽出用）"
    )

    # --- 層判定設定 ---
    # 登録者数の境界値: subscriber_count < small → small, < mid → mid, それ以外 → large
    TIER_THRESHOLDS: dict[str, int] = Field(
        default_factory=lambda: {"small": 10_000, "mid": 100_000},
        description="層判定の登録者数境界値（small/mid のキー必須）"
    )

    # 登録者数キャッシュの有効期限（日数）
    SUBSCRIBER_CACHE_DAYS: int = Field(
        7, ge=1, le=365,
        description="登録者数キャッシュを再取得するまでの日数"
    )

    # --- デフォルトジャンルマッピング ---
    DEFAULT_GENRES: dict[str, str] = Field(
        default_factory=lambda: {
            "corporate_female": "企業勢女子",
            "corporate_male": "企業勢男子",
            "indie": "個人勢",
            "english": "英語圏",
        },
        description="genres マッピングが存在しない場合のデフォルト"
    )

    # --- レートリミット時の未評価データ出力設定 ---
    PENDING_EVALUATION_DIR: str = Field(
        "pending_evaluations",
        description="Claude Code CLI 評価失敗時に未評価データを保存するディレクトリ"
    )

    model_config = SettingsConfigDict(
        env_file=Path(__file__).parent / ".env",
        env_file_encoding="utf-8",
        extra="ignore"  # 定義されていない環境変数は無視する
    )


# グローバル設定インスタンス
try:
    settings = Settings()
    # ログレベルの更新
    logging.getLogger().setLevel(settings.LOG_LEVEL)
except ValidationError as e:
    logger.critical(f"設定エラー: 環境変数が正しく設定されていません。\n{e}")
    # アプリケーションを続行できないため、ここで例外を再送出するか、sys.exitする
    # 他のモジュールがインポートした時点で落ちるようにする
    raise
